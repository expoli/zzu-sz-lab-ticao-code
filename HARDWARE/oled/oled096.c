/**********************************************
//IIC Start
**********************************************/
void IIC_Start()
{
	VL_SDA_OUT();//sda线输出
	VL_IIC_SDA=1;	  	  
	VL_IIC_SCL=1;
	delay_us(4);
 	VL_IIC_SDA=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	VL_IIC_SCL=0;//钳住I2C总线，准备发送或接收数据 
}
/**********************************************
//IIC Stop
**********************************************/
void IIC_Stop()
{
	VL_SDA_OUT();//sda线输出
	VL_IIC_SCL=0;
	VL_IIC_SDA=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	VL_IIC_SCL=1; 
	VL_IIC_SDA=1;//发送I2C总线结束信号
	delay_us(4);
}
/**********************************************
// IIC Write byte
**********************************************/
void Write_IIC_Byte(unsigned char txd)
{
	u8 t;
	
	VL_SDA_OUT();
	VL_IIC_SCL=0;//拉低时钟开始数据传输
	for(t=0;t<8;t++)
	{
		if(txd&0x80)VL_IIC_SDA=1;
		else
			VL_IIC_SDA=0;
		txd<<=1; 	  
		delay_us(2);  
		VL_IIC_SCL=1;
		delay_us(2); 
		VL_IIC_SCL=0;
		delay_us(2);
  }
  VL_IIC_SDA=1;
  delay_us(2); 
  VL_IIC_SCL=1;
  delay_us(2); 
  VL_IIC_SCL=0;
  delay_us(2); 
}

/*********************OLED写数据************************************/ 
void WriteDat(unsigned char IIC_Data)	 
{
	IIC_Start();
	Write_IIC_Byte(0x78);
	Write_IIC_Byte(0x40);			//write data
	Write_IIC_Byte(IIC_Data);
	IIC_Stop();
}
/*********************OLED写命令************************************/
void WriteCmd(unsigned char IIC_Command)
{
	IIC_Start();
	Write_IIC_Byte(0x78);            //Slave address,SA0=0
	Write_IIC_Byte(0x00);			//write command
	Write_IIC_Byte(IIC_Command);
	IIC_Stop();
}
/*********************OLED 设置坐标************************************/
void OLED_SetPos(unsigned char x, unsigned char y) //设置起始点坐标
{ 
	WriteCmd(0xb0+y);
	WriteCmd(((x&0xf0)>>4)|0x10);
	WriteCmd((x&0x0f)|0x01);
}
/*********************OLED全屏************************************/
void OLED_Fill(unsigned char fill_Data)//全屏填充
{
	unsigned char m,n;
	for(m=0;m<8;m++)
	{
		WriteCmd(0xb0+m);       //page0-page1
		WriteCmd(0x00);     //low column start address
		WriteCmd(0x10);     //high column start address
		for(n=0;n<128;n++)
		{
			WriteDat(fill_Data);
		}
	}
}
/*********************OLED复位************************************/
void OLED_CLS(void)//清屏
{
	OLED_Fill(0x00);
}
